import math

# --------------------------------

# math.ceil 向上取整
a = math.ceil(1.2)
# print(a)
# 2
b = math.ceil(1.5)
# print(b)
# 2
c = math.ceil(1.8)
# print(c)
# 2
d = math.ceil(-1.2)
# print(d)
# -1
e = math.ceil(-1.5)
# print(e)
# -1
f = math.ceil(-1.8)
# print(f)
# -1

# --------------------------------

